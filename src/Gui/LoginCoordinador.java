 public class Login2 implements ActionListener {

        @Override
        public void actionPerformed(ActionEvent ae) {
            try {
            
                String Usuario = UserField.getText().trim();
                String Password = PasswordField.getText().trim();
                
                Docente docente = College.BuscaDocente(Long.parseLong(Usuario));
               
                if (docente.getPassword().equals(Password)){
                    College.logueado1 = docente;
                    ventanaPrincipal.mostrarventanaP(docente);
                    ventanaPrincipal.getCancelar().setEnabled(true);
                   
                  UserField.setText("");
                  PasswordField.setText("");
                }else{
                    JOptionPane.showMessageDialog(null, "El password esta incorrecto");
                }
            } catch (NumberFormatException | ObjectNotFoundException | HeadlessException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }

        }


    }

